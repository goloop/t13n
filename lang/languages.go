// LanguageRulesMethod a type of special function that can correct
// transliteration according to the specifics of the specified language.
type LanguageRules func(rune, rune, rune, bool) (string, int, bool)

// The defaultLanguageRules a function that uses general correction rules
// for the default language.
func defaultLanguageRules(p, c, n rune, b bool) (string, int, bool) {
	return "", 0, false
}

// Rules returns the corresponding transliteration
// correction function for the specified language.
func Rules(lang string) LanguageRules {
	var languageRules LanguageRules

	switch lang {
	case Bg:
		languageRules = bgRules
	case Hr:
		languageRules = hrRules
	case Da:
		languageRules = daRules
	case Eo:
		languageRules = eoRules
	case De:
		languageRules = deRules
	case Hu:
		languageRules = huRules
	case Mk:
		languageRules = mkRules
	case Nb:
		languageRules = nbRules
	case Ru:
		languageRules = ruRules
	case Sr:
		languageRules = srRules
	case Sl:
		languageRules = slRules
	case Sv:
		languageRules = svRules
	case Uk:
		languageRules = ukRules
	default:
		languageRules = defaultLanguageRules
	}

	return languageRules
}
